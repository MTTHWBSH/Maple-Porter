// Ajax Pagination

jQuery(document).ready(function($){
    $('.older a').on('click', function(e)  {
        e.preventDefault();
        $('.navigation').prepend("<div class=\"loader\">&nbsp;</div>");
        
        var link = $(this).attr('href');
        
        var $content = '#work_items';
        var $nav_wrap = '.navigation';
        var $anchor = '.navigation .older a';
        var $next_href = $($anchor).attr('href'); // Get URL for the next set of posts
        
        $.get(link+'', function(data){
            var $timestamp = new Date().getTime();
            var $new_content = $($content, data).wrapInner('').html(); // Grab just the content
            
            $('.navigation .loader').remove();
            $next_href = $($anchor, data).attr('href'); // Get the new href
            $($nav_wrap).before($new_content); // Append the new content
            $('#rtz-' + $timestamp).hide().fadeIn('slow'); // Animate load
            
            //if the next href is undefined, we know we've reached the end, so switch out the button
            if( $next_href ){
                $('.older a').attr('href', $next_href); // Change the next URL
            } else {
                $('.older a').remove();
                $('.older').append("<div class=\"btn disabled\">no more posts</div>");
            }
            
            $('#work_items .navigation:last').remove(); // Remove the original navigation
        });//get
        
        //after we've rendered the requested content, check to see if that was the last page
    });//older click
});
